# there are various data structures in python , some of them are

#1) tuple: tuples are immutable, it means they cannot be changed.
tup=("blue","black","brown")
print(type(tup))

#2) set
set1={"blue","black","brown"}
print(type(set1))

#3) list
list=["blue","black","brown","orange"]
print(type(list))

#4) dictionary: it consists of key value pairs.
dict={
    'color1':'blue',
    'color2':'black',
    'color3':'brown',
    'color4':'red'
}
print(type(dict))