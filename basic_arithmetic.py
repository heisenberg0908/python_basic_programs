# to find sum:
def sum(a,b):
    return a+b

# to find product:
def product(a,b):
    return a*b

# division:
def divide(a,b):
    return a/b

# to find exponential:
def expo(a,b):
    return a**b

# to find remainder
def remainder(a,b):
    return a%b

# to find round of quotient(division):
def divide2(a,b):
    return a//b